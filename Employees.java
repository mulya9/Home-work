public class Employees {
   final String full_name;
   final String job_title;
   final int email;
   final int telephone;
   final int age;

    public Employees(String full_name, String job_title, int email, int telephone, int age) {
        this.full_name = full_name;
        this.job_title = job_title;
        this.email = email;
        this.telephone = telephone;
        this.age = age;
    }
}
